public class cast{
    public static void main(String[] args) {
        // Widening Type Casting (Into a larger datatype)
        int myInt = 9;
        double myDouble = myInt;
        System.out.println(myDouble);
        //Narrowing Type Casting into a smaller datatype
        double myNum = 99;
        int myNum2 = (int) myNum;
        System.out.println(myNum); 
        
    } 
}

