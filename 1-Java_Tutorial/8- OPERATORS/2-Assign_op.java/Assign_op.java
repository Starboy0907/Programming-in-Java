
public class Assign_op {
    public static void main(String[] args) {
        //this is the assignment operator
        int x = 10;
        //this is the addition assignment operator
        x += 5;
        System.out.println(x);
        x = 5;
        System.out.println(x);
        x += 3;
        System.out.println(x);
        x -=3;
        System.out.println(x);
        x *=3;
        System.out.println(x);
        x /=3;
        System.out.println(x);
        x %=3;
        System.out.println(x);
        x &=3;
        System.out.println(x);
        x|=3;
        System.out.println(x);
        x^=3;
        System.out.println(x);
        x>>=3;
        System.out.println(x);
        x <<=3;
        System.out.println(x);



    }
    
}
